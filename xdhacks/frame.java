    public void loadData() {
        
        //ability to reference text file no matter where program is stored(relative path)
        String currentDirectory = System.getProperty("user.dir");
        String fullFileName = currentDirectory + "\\src\\xdhacks\\traffic.txt";
      
        
        try {   
                FileReader fr = new FileReader(fullFileName);
                BufferedReader br = new BufferedReader(fr);
                String line="";
                carCount1=Integer.parseInt(br.readLine());
                busCount1=Integer.parseInt(br.readLine());
                br.close();
        }
            catch(Exception e) {
                System.out.println("Something went wrong file reading!");
        }
    }
    
    public void writeData(int result){
        
        
        String currentDirectory = System.getProperty("user.dir");
        String fullFileName = currentDirectory + "\\src\\xdhacks\\results.txt";
        
        try {
            FileWriter fw = new FileWriter(fullFileName);
            fw.flush();
            BufferedWriter br = new BufferedWriter(fw);
            br.write("" + result);        //add a newline character
           
            br.close();
            
        }
        catch(Exception e) {
            System.out.println("Error writing to file");
        }
    }
    
    public void Return(){
        
        double val1=(double)carCount1+(busCount1*1.1);
        double val2=(double)carCount2+(busCount2*1.1);
        if((val1>val2)&&(busCount1>0||busCount2>0)){
            textResult.setText("Lane 1 may pass!");
            this.writeData(1);
                           
        }else if((val1<val2)&&(busCount1>0||busCount2>0)){
            textResult.setText("Lane 2 may pass!");
            this.writeData(2);
        }
        else{
            textResult.setText("Proceed per usual");
            this.writeData(0);
        }
        
    }
   
    
