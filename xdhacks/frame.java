    private void buttonStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonStartActionPerformed
      
        if(boxMode.getSelectedItem().equals("Manual")){
            if(running==false){
       
                carCount1 = Integer.parseInt(textCar1.getText());
                carCount2 = Integer.parseInt(textCar2.getText());
       
                busCount1 = Integer.parseInt(textBus1.getText());
                busCount2 = Integer.parseInt(textBus2.getText());
                
                textCar1.setEnabled(false);
                textCar2.setEnabled(false);
                textBus1.setEnabled(false);
                textBus2.setEnabled(false);
               
                Return();
                
                running=true;
                buttonStart.setText("Restart");
            } else {
                textCar1.setText("0");
                textCar2.setText("0");
                textBus1.setText("0");
                textBus2.setText("0");
                
                carCount1 = 0;
                carCount2 = 0;
                busCount1 = 0;
                busCount2 = 0;
                textCar1.setEnabled(true);
                textCar2.setEnabled(true);
                textBus1.setEnabled(true);
                textBus2.setEnabled(true);
                running=false;
                buttonStart.setText("Start");
            }
        } else if(boxMode.getSelectedItem().equals("Auto")){
          
            if(running==false){
                loadData();
                carCount2 = Integer.parseInt(textCar2.getText());
                busCount2 = Integer.parseInt(textBus2.getText());
       
                textCar1.setEnabled(false);
                textCar2.setEnabled(false);
                textBus1.setEnabled(false);
                textBus2.setEnabled(false);
                Return();
                running=true;
                buttonStart.setText("Restart");
                
            } else {
                textCar1.setText("0");
                textCar2.setText("0");
                textBus1.setText("0");
                textBus2.setText("0");
                carCount1 = 0;
                carCount2 = 0;
                busCount1 = 0;
                busCount2 = 0;
                textCar1.setEnabled(true);
                textCar2.setEnabled(true);
                textBus1.setEnabled(true);
                textBus2.setEnabled(true);
                running=false;
                buttonStart.setText("Start");
            }   
        }  
    }//GEN-LAST:event_buttonStartActionPerformed

